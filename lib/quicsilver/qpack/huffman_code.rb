      EOS = [0x3fffffff, 30] # |11111111|11111111|11111111|111111

      REVERSE_TABLE = TABLE.each_with_object({}) do |(byte, (code, length)), tree|
        node = tree
        # Walk bits MSB-first
        (length - 1).downto(0) do |i|
          bit = (code >> i) & 1
          node[bit] ||= {}
          node = node[bit]
        end
        node[:value] = byte
      end


      class << self
        def encode(data)
          bits = 0
          bit_length = 0

          data.each_byte do |byte|
            code, length = TABLE[byte]
            bits = (bits << length) | code
            bit_length += length
          end

          # Pad with EOS prefix (1-bits) to next byte boundary
          padding = (8 - (bit_length % 8)) % 8
          bits = (bits << padding) | ((1 << padding) - 1)
          bit_length += padding

          # Pack into bytes
          byte_count = bit_length / 8
          bytes = Array.new(byte_count)
          (byte_count - 1).downto(0) do |i|
            bytes[i] = bits & 0xff
            bits >>= 8
          end
          bytes.pack("C*")
        end

        def decode(encoded_data)
          return "".b if encoded_data.empty?

          node = REVERSE_TABLE
          output = []
          # For each symbol, track where it started so we can detect
          # symbols that span the data/padding boundary
          symbol_start_bit = 0
          bit_pos = 0
          # Snapshot output length at each point a new symbol starts,
          # so we can roll back symbols that began in the padding zone
          output_at_symbol_start = 0

          encoded_data.each_byte do |byte|
            7.downto(0) do |i|
              bit = (byte >> i) & 1
              next_node = node[bit]
              return nil unless next_node # invalid code
              bit_pos += 1

              if next_node.key?(:value)
                output << next_node[:value]
                node = REVERSE_TABLE
                output_at_symbol_start = output.length
                symbol_start_bit = bit_pos
              else
                node = next_node
              end
            end
          end

          # Padding validation (RFC 7541 §5.2):
          # Bits after the last complete symbol are padding.
          padding_bits = bit_pos - symbol_start_bit
          return nil if padding_bits >= 8

          # Padding bits must all be 1
          if padding_bits > 0
            last_byte = encoded_data.getbyte(-1)
            mask = (1 << padding_bits) - 1
            return nil if (last_byte & mask) != mask
          end

          output.pack("C*")
        end
      end
